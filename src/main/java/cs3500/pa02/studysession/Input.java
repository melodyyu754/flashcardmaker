package cs3500.pa02.studysession;

/**
 * Enumeration for Input, containing 4 options for input.
 */
public enum Input {
  /**
   * EASY: an option for user input when the flashcard is easy
   */
  EASY,
  /**
   * HARD: an option for user input when the flashcard is hard
   */
  HARD,
  /**
   * SHOWANSWER: an option for user input when user wants to see the answer
   */
  SHOWANSWER,
  /**
   * EXIT: an option for user input when the user wants to exit the review session.
   */
  EXIT
}